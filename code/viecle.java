package AssignmentPackage;

public class viecle {
	int id ;
	int model_year ;
	String nodel_name ;
	double width , depth ;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getModel_year() {
		return model_year;
	}
	public void setModel_year(int model_year) {
		this.model_year = model_year;
	}
	public String getNodel_name() {
		return nodel_name;
	}
	public void setNodel_name(String nodel_name) {
		this.nodel_name = nodel_name;
	}
	public double getWidth() {
		return width;
	}
	public void setWidth(double width) {
		this.width = width;
	}
	public double getDepth() {
		return depth;
	}
	public void setDepth(double depth) {
		this.depth = depth;
	}
	

}
